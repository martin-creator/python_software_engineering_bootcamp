# Types of string formating
# String formatting is a way to format strings in Python. There are three ways to perform string formatting.
# Formatting with the % operator
# Formatting with the format() method
# Formatting with f-strings
#
# Example 1: Formatting with the % operator
name = "John"
age = 23
print("My name is %s and I am %d years old" % (name, age))    # Output: My name is John and I am 23 years old

# Example 2: Formatting with the format() method
name = "John"
age = 23
print("My name is {} and I am {} years old".format(name, age))    # Output: My name is John and I am 23 years old

# Example 3: Formatting with f-strings
name = "John"
age = 23
print(f"My name is {name} and I am {age} years old")    # Output: My name is John and I am 23 years old


