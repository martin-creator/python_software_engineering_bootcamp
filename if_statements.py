# They are used to control the flow of a program.
# The if statement is used to test a condition. If the condition is true, the code block following the if statement is executed.
# If the condition is false, the code block following the if statement is skipped and the code block following the else statement is executed.
# The else statement is used to execute a block of code if the condition is false.
# The elif statement is used to test multiple conditions.
# The elif statement is used to check multiple expressions for TRUE and execute a block of code as soon as one of the conditions evaluates to TRUE.
# The elif statement is short for "else if".
# The else statement is used to execute a block of code if the condition is false.
