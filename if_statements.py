# Example 3: Using the if else statement
    
x = 10
y = 20

if x > y:
    print("x is greater than y")
else:
    print("x is less than y")


# Output: x is less than y
    
# Example 4: Using the if elif else statement
x = 10
y = 20

if x > y:
    print("x is greater than y")
elif x < y:
    print("x is less than y")
else:
    print("x is equal to y")

# Output: x is less than y

